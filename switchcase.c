// Switch case by comparing one number to another numbers
#include<stdio.h>
int main(){
  int number;
   printf("Enter a number");
   scanf("%d",&number);
   switch(number){
	   case 10:
	   printf("Is Number is equals to 10");
	   break;
	   case 20:
	   printf("IsNumber is Equals to 20");
	   break;
	   case 30:
	   printf("Is Number IS Equals to 30");
	   break;
	   case 40:
	   printf("Is Number is Equals to 40");
	   break;
	   default:
	   printf("Is Not Equals to anything");
   }
	   
   
   
}
