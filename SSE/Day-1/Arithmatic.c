//Arithmatic operation-->+,-,/,*,%,
#include<stdio.h>
int main(){
	int a,b;
	printf("Enter Two Numbers to find arithmatic operations:");
	scanf("%d%d",&a,&b);
	printf("Addition of %d and %d is %d\n",a,b,a+b);
	printf("subtration of %d and %d is %d\n",a,b,a-b);
	printf("multiplication of %d and %d is %d\n",a,b,a*b);
	printf("divison of %d and %d is %d\n",a,b, a/b);
	printf("module divison of %d and %d is %d\n",a,b,a%b);
	return 0;
}





