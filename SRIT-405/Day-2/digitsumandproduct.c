//~ input:5741
//~ output:5+7+4+1-->17
	   //~ 5*7*4*1-->140
#include<stdio.h>
int main(){
	int n,sum=0,pro=1;
	scanf("%d",&n);//5741
	while(n>0){//(5741>0(T),574>0(T),57>0(T),5>0(T),0>0(F))
		int la=n%10;//5741%10-->1,574%10-->4,57%10-->7,5%10-->5
		sum=sum+la;//0+1-->1,1+4-->5,5+7-->12,12+5-->17
		pro=pro*la;//1*1-->1,1*4-->4,4*7-->28,28*5-->140
		n=n/10;//5741/10-->574,574/10-->57,57/10-->5,5/10-->0
	}
	printf("Sum is %d\n",sum);//17
	printf("Product is %d\n",pro);//140
	return 0;
}
