#include<stdio.h>
int main(){
	int num,count=0,sum=0;
	printf("Enter One value:");// 1 * 2 * 3 * 4 * 5-->120
	scanf("%d",&num);
	while(num>0){//12345>0(T),1234>0(T),123>0(T),12>0(T),0>0(F)
		int last=num%10;//12345%10-->5,1234%10-->4,123%10-->3,12%10->2,1%10-->1
		sum=sum+last;//1*5-->5,5*4-->20,20*3-->60,60*2-->120,120*1-->120
		num=num/10;//12345/10-->1234,1234/10-->123,123/10-->12,12/10-->1,1/10-->0
		count++;//1,2,3,4,5
	}
	printf("addition =%d\n",sum);
	printf("Count= %d \n",count);
	return 0;
}

